export function parsePrepareProductionEnvArgs(argv = process.argv.slice(2)) {
  const readArg = (name) => {
    const prefix = `${name}=`;
    const arg = argv.find((entry) => entry.startsWith(prefix));
    return arg ? arg.slice(prefix.length) : undefined;
  };

  return {
    appBaseUrl: readArg("--app-base-url"),
    approvalRecipients: readArg("--approval-recipients"),
    approvalSender: readArg("--approval-sender"),
    force: argv.includes("--force"),
    notificationsEnabled: argv.includes("--notifications-enabled"),
    output: readArg("--output") ?? ".env.production.local",
    serviceAccount: readArg("--service-account"),
    source: readArg("--source") ?? ".env.local",
  };
}

export function buildProductionEnv({
  appBaseUrl,
  approvalRecipients,
  approvalSender,
  notificationsEnabled = false,
  serviceAccount,
  sourceEnv,
}) {
  const errors = [];
  const output = {};

  if (!appBaseUrl) {
    errors.push("--app-base-url is required.");
  } else {
    try {
      const url = new URL(appBaseUrl);
      if (url.protocol !== "https:") {
        errors.push("--app-base-url must be an https URL.");
      }
    } catch {
      errors.push("--app-base-url must be a valid URL.");
    }
  }

  for (const key of COPY_KEYS) {
    const value = readString(sourceEnv[key]);
    if (value) {
      output[key] = value;
    }
  }

  for (const key of REQUIRED_KEYS) {
    if (!readString(output[key])) {
      errors.push(`${key} must be set in the source env.`);
    }
  }

  output.APP_BASE_URL = appBaseUrl ?? "";
  output.ASK_DEMO_MODE = "false";
  output.IMAGE_STORE = "drive";
  output.KB_APPROVAL_NOTIFICATIONS_ENABLED = notificationsEnabled ? "true" : "false";
  output.LOCAL_DEMO_AUTH = "false";
  output.MODEL_PROVIDER = "gemini";

  if (serviceAccount) {
    output.CLOUD_RUN_SERVICE_ACCOUNT = serviceAccount;
  }

  if (notificationsEnabled) {
    if (!approvalSender) {
      errors.push("--approval-sender is required with --notifications-enabled.");
    }
    if (!approvalRecipients) {
      errors.push("--approval-recipients is required with --notifications-enabled.");
    }
    if (approvalSender) {
      output.KB_APPROVAL_SENDER = approvalSender;
    }
    if (approvalRecipients) {
      output.KB_APPROVAL_RECIPIENTS = approvalRecipients;
    }
  }

  for (const key of FORBIDDEN_OUTPUT_KEYS) {
    delete output[key];
  }

  return { errors, ok: errors.length === 0, output };
}

export function parseEnvText(text) {
  const result = {};

  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) {
      continue;
    }

    const separator = trimmed.indexOf("=");
    if (separator < 1) {
      continue;
    }

    const key = trimmed.slice(0, separator).trim();
    const value = trimmed.slice(separator + 1).trim();
    result[key] = unquote(value);
  }

  return result;
}

export function serializeProductionEnv(env) {
  const lines = [
    "# Generated by npm run prepare:production-env. Gitignored; do not commit.",
    "# Contains production identifiers only. Runtime secrets stay in Secret Manager.",
    "",
  ];

  for (const key of Object.keys(env).sort()) {
    const value = String(env[key]);
    if (/\r|\n/.test(value)) {
      throw new Error(`${key} contains a newline and cannot be serialized.`);
    }
    lines.push(`${key}=${value}`);
  }

  return `${lines.join("\n")}\n`;
}

export function main(argv = process.argv.slice(2)) {
  const args = parsePrepareProductionEnvArgs(argv);
  const sourcePath = resolve(root, args.source);
  const outputPath = resolve(root, args.output);

  if (!existsSync(sourcePath)) {
    throw new Error(`${args.source}: not found`);
  }
  if (existsSync(outputPath) && !args.force) {
    throw new Error(`${args.output}: already exists; pass --force to replace it.`);
  }

  const sourceEnv = parseEnvText(readFileSync(sourcePath, "utf8"));
  const result = buildProductionEnv({
    appBaseUrl: args.appBaseUrl,
    approvalRecipients: args.approvalRecipients,
    approvalSender: args.approvalSender,
    notificationsEnabled: args.notificationsEnabled,
    serviceAccount: args.serviceAccount,
    sourceEnv,
  });

  if (!result.ok) {
    throw new Error(
      `Production env preparation failed:\n- ${result.errors.join("\n- ")}`,
    );
  }

  writeFileSync(outputPath, serializeProductionEnv(result.output), {
    encoding: "utf8",
    flag: "w",
  });
  console.log(
    `Prepared ${args.output} with ${Object.keys(result.output).length} production-safe variables; notifications ${args.notificationsEnabled ? "enabled" : "disabled"}; no runtime secrets copied.`,
  );
}

function readString(value) {
  return typeof value === "string" && value.trim() ? value.trim() : undefined;
}

function unquote(value) {
  if (
    value.length >= 2 &&
    ((value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'")))
  ) {
    return value.slice(1, -1);
  }
  return value;
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  try {
    main();
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
  }
}
