function canonicalGuideReference(reference) {
  if (!reference.startsWith("guide#")) return reference;
  const value = reference.slice("guide#".length);
  const [root, ...rest] = value.split(".");
  const alias = GUIDE_REFERENCE_ALIASES[root];
  if (!alias) return reference;
  return `guide#${alias}${rest.length > 0 ? `.${rest.join(".")}` : ""}`;
}

function canonicalReviewerReference(reference) {
  return REVIEWER_REFERENCE_ALIASES[reference] ?? reference;
}

function auditCase({
  id,
  title,
  surface,
  route,
  process,
  workflowStage,
  role,
  dataMode,
  mutationKind = "read",
  userAction,
  validation,
  visible,
  persisted,
  handoff = "The owning record or next safe audit surface remains visible.",
  receipt,
  guideRefs,
  reviewerRefs = [],
  safeAlias = null,
  dependsOn = [],
  screenshotSafe,
}) {
  const inferredScreenshotSafety =
    dataMode === "test" &&
    (surface === "Lease Test" ||
      surface === "Vendor portal mailbox" ||
      (surface === "Maintenance Test" && route.includes("[ticketId]")));
  const normalizedReviewerRefs = Object.freeze([
    ...new Set(
      [
        ...reviewerRefs,
        ...(ADDITIONAL_REVIEWER_REFS_BY_CASE_ID[id] ?? []),
        ...guideRefs.filter((reference) => reference.startsWith("reviewer-pass#")),
      ].map(canonicalReviewerReference),
    ),
  ]);
  const normalizedGuideRefs = Object.freeze(
    guideRefs
      .filter((reference) => !reference.startsWith("reviewer-pass#"))
      .map(canonicalGuideReference),
  );
  return Object.freeze({
    id,
    title,
    surface,
    route,
    process,
    workflow_stage: workflowStage,
    role,
    data_mode: dataMode,
    mutation_kind: mutationKind,
    safe_alias: safeAlias,
    depends_on: Object.freeze(dependsOn),
    screenshot_safe: screenshotSafe ?? inferredScreenshotSafety,
    guide_refs: normalizedGuideRefs,
    reviewer_refs: normalizedReviewerRefs,
    expected: Object.freeze({
      user_action: userAction,
      preconditions_and_role: `${role}; use only the documented ${dataMode} boundary.`,
      data_mode: dataMode,
      app_validation: validation,
      visible_result: visible,
      persisted_change: persisted ?? defaultPersistedChange(mutationKind),
      downstream_handoff: handoff,
      receipt_audit_or_idempotency_evidence:
        receipt ?? defaultReceiptEvidence(mutationKind),
    }),
  });
}

function defaultPersistedChange(mutationKind) {
  switch (mutationKind) {
    case "read":
      return "No persisted change is expected.";
    case "test_write":
      return "One isolated Test/app state transition is expected when the safe mutation is authorized; a blocked guard leaves state unchanged.";
    case "test_create":
      return "One isolated Test record is expected after the checkpointed create; resume must adopt it rather than creating another.";
    case "test_action":
      return "One idempotent Test attempt and simulated receipt are expected; no provider state changes.";
    case "app_write":
      return "One app-owned state or audit transition is expected when authorized; no provider effect is implied.";
    case "reversible_app_write":
      return "One reversible app-owned change and its restoration are expected; final readback must match the captured baseline.";
    case "live_effect":
      return "Only an explicitly authorized exact provider effect may persist; a blocked boundary leaves provider and app state unchanged.";
    default:
      throw new Error(`Unsupported mutation kind: ${mutationKind}`);
  }
}

function defaultReceiptEvidence(mutationKind) {
  switch (mutationKind) {
    case "read":
      return "No receipt is expected for this read-only interaction.";
    case "test_write":
      return "The checkpointed mutation key plus bodyless app activity or persisted-state readback is required.";
    case "test_create":
      return "The checkpointed create key, reserved alias, generated Test record ID, and creation readback are required.";
    case "test_action":
      return "One bodyless Test attempt and receipt must bind the exact record, action, target, and audit run without Live-proof eligibility.";
    case "app_write":
      return "A bodyless app audit/activity record and persisted-state readback must bind the exact decision and actor.";
    case "reversible_app_write":
      return "Bodyless before/change/restore readbacks must prove the exact setting returned to its captured baseline.";
    case "live_effect":
      return "An exact bodyless provider receipt/readback or explicit ambiguity record is required before any retry.";
    default:
      throw new Error(`Unsupported mutation kind: ${mutationKind}`);
  }
}

